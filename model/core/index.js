
`use strict`

//......................................................................................................................

onresize = unused => dunp.changeScene(project.states.temp.scene.id)

//......................................................................................................................

project.begin = () =>
{
  dunp.get(`title`).innerHTML = dunp.getLang().title
  dunp.changeScene(`main`)
}

